package basic;

public class OprTest01 {
	public static void main(String[] args) 	{
		int y;
		int x;
		x=5;
		y= x++;
		System.out.println(""+x+y);
		x=5;
		y= ++x;
		System.out.println(""+x+y);
		x=5;
		y= x--;
		System.out.println(""+x+y);
		x=5;
		y= --x;
		System.out.println(""+x+y);
	

		
		
		
		
	}
}
